public class DataTypesAndVariables {
    public static void main(String[] args) {
        // Variables : where values can be stored.

        // Primitive Datatypes and Declaring variables
        byte b; // b is a variable of type byte.
        short s; // s is a variable of type short.
        int i; // i is a variable of type int.
        long l; // l is a variable of type long.
        float f; // f is a variable of type float.
        double d; // d is variable of type double.
        boolean bool; // bool is a variable of type boolean.
        char c; // c is a variable of type char.

        // Initializing variables
        b = 127;
        s = 32767;
        i = 20;
        l = 2000000000;
        f = 1.2345F;
        d = 1.2345678999;
        bool = true;
        c = 'n';

        // Declaring and Initializing in the same line
        int a = 10;

        // Multiple variables declaration in same line
        int p, q, r; // use comma (,) to separate variables

        // Declaration and Initialization multiple variables
        int m = 10, n = 2, o = 5; // use comma (,) to separate variables

        // Printing variables
        System.out.println(m);

        // Multiple Variable Printing
        System.out.println(m + " " + n + " " + o); // use + to concatenate values
        System.out.println(m+n); // + will add variable values
    }
}
