package apna.practice.problems;
import java.util.*;


public class sumOfNums {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        // Taking 2 Nums inputs
        int a = sc.nextInt();
        int b = sc.nextInt();

        int sum = a + b;

        // Printing 2 nums
        System.out.println("Sum of a + b = " + sum);
    }
}
