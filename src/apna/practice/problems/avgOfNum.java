package apna.practice.problems;

public class avgOfNum {

    public static void main(String[] args) {
        // Average of Numbers
        int a, b, c;
        a = 10;
        b = 20;
        c = 30;

        int avg = (a + b + c) / 3;
        System.out.println("Average of Nums is: " + avg);
    }
}
